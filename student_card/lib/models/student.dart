class Student {
  final String id;
  final String name;
  final String colge;
  final String? phoneNumber;
  final String address;
  final String age;
  final String issueDate;
  final String expiryDate;

  Student({
    required this.id,
    required this.name,
    required this.phoneNumber,
    required this.colge,
    required this.address,
    required this.age,
    required this.issueDate,
    required this.expiryDate,
  });
}
